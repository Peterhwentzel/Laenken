		<h3>Sådan starter behandlingen</h3>
		<p>Når du vælger at starte i alkoholbehandling, ringer du og aftaler en tid eller møder op på et af Lænke-ambulatorierne. <br>
		Du vil ved første møde få en samtale med en alkoholbehandler eller en læge.<br>										
		Inden for ca. en uge vil du få en konsultation hos ambulatoriets læge.<br>										
		Har du i den seneste tid drukket meget, har du typisk brug for abstinensbehandling inden samtalebehandlingen kan påbegyndes. Abstinensmedicin tages dagligt i 2-10 dage.<br>								
		Gennem de første samtaler finder vi sammen ud af, hvordan Lænke-ambulatoriet bedst kan hjælpe dig. Vi vil spørge ind til væsentlige områder i din livssituation, herunder om du har haft eller har sygdomme, der bør tages højde for i behandlingen.<br>								
		Som et muligt supplement til samtalebehandlingen kan antabus også opstartes med det samme. Antabus gives ved personligt fremmøde to gange om ugen – og kan gives i en længere periode, fx 1 år.
		</p>

		<p><b>Du vil gennem hele din behandling have jævnlige samtaler med din alkoholbehandler.</b><br />
		Typiske samtaleemner er fx:</p>
		<ul>
			<li><p>Håndtering af trangen til at drikke</p></li>
			<li><p>Forebyggelse af tilbagefald</p></li>
			<li><p>Vejledning i opbygningen af et socialt liv med andre alkoholvaner</p></li>
		</ul>
		<p>Der kan være elementer af undervisning<br>
